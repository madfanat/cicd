def area(a, b):
    '''
    Принимает a, b (float) - длины высоты и ширины прямоугольника
    Возвращает площадь прямоугольника со сторонами a, b (float)
    '''
    return a * b


def perimeter(a, b):
    '''
    Принимает a, b (float) - длины высоты и ширины прямоугольника
    Возвращает периметр прямоугольника со сторонами a, b (float)
    '''
    return 2 * (a + b)