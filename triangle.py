def area(a, h):
    '''
    Принимает a, h (float) - длины стороны треугольника и соответсвующей высоты
    Возвращает площадь треугольника со стороной a и высотой h (float)
    '''
    return a * h / 2


def perimeter(a, b, c):
    '''
    Принимает a, b, c (float) - длины сторон треугольника
    Возвращает периметр треугольника со сторонами a, b, c (float)
    '''
    return a + b + c